// Include necessary header files
#include<stdio.h>
#include<math.h>

int main(){
    // Basic mathematical functions demonstration
    
    // Square root function
    printf("The square root of 25 is %f\n", sqrt(25));
    
    // Power function (base^exponent)
    printf("The power of 2 to the power of 3 is %f\n", pow(2, 3));
    
    // Absolute value function
    printf("The absolute value of -10 is %f\n", abs(-10));
    
    // Ceiling function (rounds up to nearest integer)
    printf("The ceil of 2.3 is %f\n", ceil(2.3));
    
    // Floor function (rounds down to nearest integer)
    printf("The floor of 2.3 is %f\n", floor(2.3));
    
    // Round function (rounds to nearest integer)
    printf("The round of 2.3 is %f\n", round(2.3));
    
    // Trigonometric functions
    printf("The sin of 2.3 is %f\n", sin(2.3));
    printf("The cos of 2.3 is %f\n", cos(2.3));
    printf("The tan of 2.3 is %f\n", tan(2.3));
    
    // Inverse trigonometric functions
    printf("The asin of 2.3 is %f\n", asin(2.3));
    printf("The acos of 2.3 is %f\n", acos(2.3));
    printf("The atan of 2.3 is %f\n", atan(2.3));
    
    // Hyperbolic functions
    printf("The sinh of 2.3 is %f\n", sinh(2.3));
    printf("The cosh of 2.3 is %f\n", cosh(2.3));
    printf("The tanh of 2.3 is %f\n", tanh(2.3));
    
    // Exponential function (e^x)
    printf("The exp of 2.3 is %f\n", exp(2.3));
    
    return 0;
}